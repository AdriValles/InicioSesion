    private void jButtonAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAceptarActionPerformed
        // TODO add your handling code here: 
        String user = jTextFieldusuario.getText();
        String contrasena = new String(jPasswordFieldContrasena.getPassword());
        String contrasena2 = new String(jPasswordFieldContrasena2.getPassword());
        int edad = (Integer) jSpinnerEdad.getValue();
        String lugarNacimiento = (String) jComboBox1.getSelectedItem();
        Date fechaRegistro = jDateChooserRegistro.getDate(); // Asume que `fechaRegistro` es tipo `Date`
        String trato = this.trato; // Sr. o Sra.

        if (!contrasena.equals(contrasena2)) {
            JOptionPane.showMessageDialog(this, "Las contraseñas no coinciden. Por favor, verifique.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        } else if (user.equals("")) {
            JOptionPane.showMessageDialog(this, "No puedes dejar vacío el usuario", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        } else if (contrasena.equals("")) {
            JOptionPane.showMessageDialog(this, "No puedes dejar vacía la contraseña", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        } else if (fechaRegistro == null) {
            JOptionPane.showMessageDialog(this, "Tienes que seleccionar una fecha de registro", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        } else if (!jCheckBoxTerminosCondiciones.isSelected()) {
            JOptionPane.showMessageDialog(this, "Tienes que aceptar los términos y condiciones para poder continuar", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        } else if (trato == null) {
            JOptionPane.showMessageDialog(this, "Tienes que seleccionar un trato", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        } else {
            int respuestaRegistro = JOptionPane.showConfirmDialog(this, "¿Esta seguro de que quiere registrar el usuario?", "Confirmación", JOptionPane.YES_NO_OPTION);
            if (respuestaRegistro != JOptionPane.YES_OPTION) {
                return;
            }
            Usuario usuario = new Usuario(trato, user, contrasena, contrasena2, edad, lugarNacimiento, ruta, fechaRegistro.toString());
            LogicaUsuarios.anadirUsuario(usuario);
            setVisible(false);
        }


    }//GEN-LAST:event_jButtonAceptarActionPerformed

