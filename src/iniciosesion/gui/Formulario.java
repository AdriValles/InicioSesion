    private void jCheckBoxTerminosCondicionesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jCheckBoxTerminosCondicionesActionPerformed
        // TODO add your handling code here:
        if (jCheckBoxTerminosCondiciones.isSelected()) {
            // Si la casilla es seleccionada, mostrar el mensaje de confirmación
            int response = JOptionPane.showConfirmDialog(this, "¿Acepta los términos y condiciones?", "Confirmación", JOptionPane.YES_NO_OPTION);

            // Si el usuario selecciona "No", desmarcar la casilla
            if (response != JOptionPane.YES_OPTION) {
                jCheckBoxTerminosCondiciones.setSelected(false);
            }
        }

    }//GEN-LAST:event_jCheckBoxTerminosCondicionesActionPerformed

