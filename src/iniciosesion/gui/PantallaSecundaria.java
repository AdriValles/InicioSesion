    public void recibirDatosDePrincipal(Usuario usuario){
        //jLabelBienvenidaUsuario.setText("Bienvenido " + usuario.getUsuario());
        jLabelTrato.setText("El trato escogido es: " + usuario.getTrato());
        jLabelUsuario.setText("Usuario: " + usuario.getUsuario());
        jLabelContrasena.setText("Contrase√±a: " + usuario.getContrasena());
        jLabelEdad.setText("Edad: " + usuario.getEdad());
        jLabelNacimiento.setText("Lugar de nacimiento: " + usuario.getNacimiento());
        jLabelFechaRegistro.setText("Fecha de registro: " + usuario.getFecharegistro());
        
        ImageIcon icon = new ImageIcon(usuario.getRutaImagen());
            Image imagen = icon.getImage();
            Image imagenEscalada = imagen.getScaledInstance(jLabelImagen.getWidth(), jLabelImagen.getHeight(), Image.SCALE_DEFAULT);
            ImageIcon iconoAdaptado = new ImageIcon(imagenEscalada);
        jLabelImagen.setIcon(iconoAdaptado);
        
    }

